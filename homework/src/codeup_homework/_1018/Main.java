package codeup_homework._1018;

import java.util.Scanner;

public class Main {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);

        String a = sc.nextLine();

        a. toCharArray();

        for (int i = 0; i < a.length(); i++) {
            System.out.print(a.charAt(i));
        }

        sc.close();
    }
}
